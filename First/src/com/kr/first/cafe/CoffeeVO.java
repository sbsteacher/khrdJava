package com.kr.first.cafe;

public class CoffeeVO {
	private int i_coffee;
	private String nm;
	private int price;
	
	public int getI_coffee() {
		return i_coffee;
	}
	public void setI_coffee(int i_coffee) {
		this.i_coffee = i_coffee;
	}
	public String getNm() {
		return nm;
	}
	public void setNm(String nm) {
		this.nm = nm;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
}
